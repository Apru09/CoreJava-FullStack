public class DataTypes {
    
    public static void main(String[] args) {

        /* A "data type" is a classification of data values. 
         * 
         * A "Variable" is a container that holds the data for further manipulation.
        */


        
        byte smallNumber = 3;
        short number1 = 6;
        int number2 = 5;
        float decimalNumber1 = 4.5f;
        double decimalNumber2 = 6.9;
        char character = 'P';


        /* byte, short, int, float, double and char are data types 
         * 
         * smallNumber, number1, number2, decimalNumber1, decimalNumber2, character are variables.
        */


        System.out.println(smallNumber);
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(decimalNumber1);
        System.out.println(decimalNumber2);
        System.out.println(character);
    }
}

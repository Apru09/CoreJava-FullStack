public class EnhancedForLoop {
    
    public static void main(String[] args) {
        
        int[] array = {10,20,30,40,50};

        for(int item : array) {
            System.out.print(item + " ");
        }

        // Also called as For Each loop. 
        // Syntax -> for(Datatype variable : Array name) {Statements;}
    }
}

public class ForLoop {
    
    public static void main(String[] args) {
        
        // print same line for 5 times.

        System.out.println("Noob Master 69");
        System.out.println("Noob Master 69");
        System.out.println("Noob Master 69");
        System.out.println("Noob Master 69");
        System.out.println("Noob Master 69");

        System.out.println();

        // As it is the same task, a loop can be used to perform same task.

        for(int i=0; i<=4; i++) {
            System.out.println("Noob Master 69");
        }

        // Used when we know the number of iterations.
    }
}
